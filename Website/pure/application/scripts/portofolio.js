function iframe_builder( id ) {

	var iframe;
	var iframe_span;
	var onload;
	iframe = document.createElement( "iframe" );
	iframe.frameBorder = "0";
	if ( iframe.class = "" ) {
		iframe.class = "iframe";
	} else {
		iframe.class = iframe.class + " iframe";
	}
	iframe.scrolling = "no";
	iframe.onload = function ( e ) {
						var target = e.target;
						var id = $( target ).attr( "id" );
						id = id.split( "_iFrame" );
						id = id[0];
						keepFrameSize_onLoad( portofolio_items[ id ][ "id" ] );
					};
	iframe.src = "portofolio_item.php?type=" + portofolio_items[ id ][ "type" ] + "&id=" + portofolio_items[ id ][ "id" ];
	iframe.id = portofolio_items[ id ][ "id" ] + "_iFrame";
	
	iframe_span =  document.createElement( "span" );
	iframe_span.text = "Δυστυχώς προέκυψε κάποιο πρόβλημα με την συγκεκριμένη φωτογραφία.";
	iframe.appendChild( iframe_span );
	document.getElementById( id + "_iFrame_wrapper" ).appendChild( iframe );
}
function keepFrameSize_onLoad( id ) {
	var iFrame 	  = document.getElementById( id + "_iFrame" );
	var iframeWin = iFrame.contentWindow || iFrame.contentDocument.parentWindow;
	if ( iframeWin.document.body || iframeWin.document.documentElement ) {
		if ( portofolio_items[ id ][ "retracted" ] == "" ) {
			portofolio_items[ id ][ "retracted" ] = iframeWin.document.documentElement.scrollHeight || iframeWin.document.body.scrollHeight;
		}
		iFrame.height = portofolio_items[ id ][ "retracted" ];
	}
}
function keepFrameSize_expand( id, log_height ) {
	var iFrame 	  = document.getElementById( id + "_iFrame" );
	var iframeWin = iFrame.contentWindow || iFrame.contentDocument.parentWindow;
	if ( iframeWin.document.body || iframeWin.document.documentElement ) {
		if ( (portofolio_items[ id ][ "expanded" ] == "") || (log_height) ) {
			portofolio_items[ id ][ "expanded" ] = iframeWin.document.documentElement.scrollHeight || iframeWin.document.body.scrollHeight;
		}
		iFrame.height = portofolio_items[ id ][ "expanded" ];
	}
}
function keepFrameSize_retract( id ) {
	var iFrame 	  = document.getElementById( id + "_iFrame" );
	var iframeWin = iFrame.contentWindow || iFrame.contentDocument.parentWindow;
	if ( iframeWin.document.body || iframeWin.document.documentElement ) {
		iFrame.height = portofolio_items[ id ][ "retracted" ];
	}
}

function portofolio_item_inViewPort ( ) {
	$( ".portofolio_item:in-viewport" ).each( function( ) {
		var id = $(this).attr( "id" );
				
		id = id.split( "_iFrame_wrapper" );
		id = id[0];
		
		if ( $( "#"+id+"_iFrame_wrapper" ).hasClass( "belowTheFold" ) ) {
			$( "#"+id+"_iFrame_wrapper" ).removeClass( "belowTheFold" );
		}
		if ( $( "#"+id+"_iFrame_wrapper" ).hasClass( "aboveTheTop" ) ) {
			$( "#"+id+"_iFrame_wrapper" ).removeClass( "aboveTheTop" );
		}
		if( !($( "#"+id+"_iFrame_wrapper" ).hasClass( "inViewPort" )) ) {
			$( "#"+id+"_iFrame_wrapper" ).addClass( "inViewPort" );
		}
		if(  portofolio_items[ id ][ "activated" ] == "N" ) {
			portofolio_items[ id ][ "activated" ] = "Y";
			iframe_builder( id );
		}
	});
}
